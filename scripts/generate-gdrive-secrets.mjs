// Regenerates src/app/core/adapters/cloud/gdrive/desktop-secrets.ts from the
// GDRIVE_DESKTOP_CLIENT_ID / GDRIVE_DESKTOP_CLIENT_SECRET env vars. Runs as part
// of `pnpm run build` (mirroring generate-build-info.mjs). When the env vars are
// unset — every web build — it writes empty placeholders, which matches the
// committed copy, so there is no git noise and no secret in the repo.
//
// Only the Tauri desktop loopback flow consumes these; set the env vars only for
// desktop release builds. NEVER commit the populated file.

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const clientId = process.env.GDRIVE_DESKTOP_CLIENT_ID ?? '';
const clientSecret = process.env.GDRIVE_DESKTOP_CLIENT_SECRET ?? '';

const contents = `// AUTO-GENERATED by scripts/generate-gdrive-secrets.mjs — do not edit by hand.
// The committed copy holds EMPTY placeholders; desktop release builds overwrite
// it from the GDRIVE_DESKTOP_CLIENT_ID / GDRIVE_DESKTOP_CLIENT_SECRET env vars.
// NEVER commit real values here — this repo is public.
//
// Only the Tauri desktop (loopback Auth-Code + PKCE) flow uses these; the web
// build leaves them empty (the browser GIS flow needs no secret).

export interface DesktopOAuth {
\treadonly clientId: string;
\treadonly clientSecret: string;
}

export const DESKTOP_OAUTH: DesktopOAuth = {
\tclientId: '${clientId}',
\tclientSecret: '${clientSecret}',
};
`;

const outFile = join(
	root,
	'src',
	'app',
	'core',
	'adapters',
	'cloud',
	'gdrive',
	'desktop-secrets.ts',
);
writeFileSync(outFile, contents);
console.log(
	`desktop-secrets.ts -> clientId ${clientId ? 'set' : 'empty'}, secret ${clientSecret ? 'set' : 'empty'}`,
);
