// Regenerates src/build-info.ts with the current version, git SHA, and build
// timestamp. Runs as part of `pnpm run build` (so web, desktop, and mobile
// builds all get a stamped value). Falls back gracefully outside a git checkout.

import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));

let gitSha = 'unknown';
try {
	gitSha = execSync('git rev-parse --short HEAD', { cwd: root }).toString().trim();
} catch {
	// Not a git checkout (e.g. a release tarball) — leave as 'unknown'.
}

const builtAt = new Date().toISOString();

const contents = `// AUTO-GENERATED by scripts/generate-build-info.mjs — do not edit by hand.
// The committed copy holds dev placeholders; production builds overwrite it.

export interface BuildInfo {
\treadonly version: string;
\treadonly gitSha: string;
\t/** ISO-8601 build timestamp, or '' for an un-stamped dev build. */
\treadonly builtAt: string;
}

export const BUILD_INFO: BuildInfo = {
\tversion: '${pkg.version}',
\tgitSha: '${gitSha}',
\tbuiltAt: '${builtAt}',
};
`;

const outFile = join(root, 'src', 'build-info.ts');
writeFileSync(outFile, contents);
console.log(`build-info.ts -> v${pkg.version} (${gitSha}) @ ${builtAt}`);
